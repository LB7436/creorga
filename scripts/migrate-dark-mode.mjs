console.log(`  ✓ ${CSS_PATH}`)

// Audit white-background patterns to estimate coverage
const SCAN_DIRS = ['apps/web/src/pages', 'apps/web/src/components']
const TARGET_PATTERNS = [
  /background:\s*['"]?#fff(?:fff)?['"]?/g,
  /background:\s*['"]?white['"]?/g,
  /background-color:\s*['"]?#fff(?:fff)?['"]?/g,
]

let filesScanned = 0
let filesAffected = 0
const affected = []

function walk(dir) {
  if (!fs.existsSync(dir)) return
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name)
    if (entry.isDirectory()) walk(p)
    else if (/\.(tsx|jsx)$/.test(entry.name)) {
      filesScanned++
      const src = fs.readFileSync(p, 'utf8')
      const matches = TARGET_PATTERNS.flatMap((re) => [...src.matchAll(re)])
      if (matches.length > 0) {
        filesAffected++
        affected.push({ file: path.relative(ROOT, p), count: matches.length })
      }
    }
  }
}

for (const d of SCAN_DIRS) walk(path.resolve(d))

console.log(`\n📊 Audit :`)
console.log(`  ${filesScanned} fichiers scannés`)
console.log(`  ${filesAffected} fichiers avec fonds blancs détectés`)

const md = `# Dark Mode Migration Report\n\n**${new Date().toISOString()}**\n\n` +
  `## Résumé\n\n` +
  `- Stratégie : **CSS Overlay** (zéro modification de composant)\n` +
  `- Fichier généré : \`apps/web/src/styles/dark-overlay.css\`\n` +
  `- Activation : \`document.documentElement.setAttribute('data-theme-mode', 'dark')\`\n\n` +
  `## Fichiers contenant des fonds blancs (${filesAffected}/${filesScanned})\n\n` +
  '| Fichier | Occurrences |\n|---|---|\n' +
  affected.sort((a, b) => b.count - a.count)
    .map((a) => `| \`${a.file}\` | ${a.count} |`).join('\n') +
  `\n\n## Activation côté UI\n\n` +
  '1. Importer `dark-overlay.css` dans `main.tsx`\n' +
  '2. Étendre `themeStore` avec un toggle `darkMode: boolean`\n' +
  '3. Lier au `data-theme-mode` de `<html>`\n'

fs.mkdirSync(path.dirname(REPORT_PATH), { recursive: true })
fs.writeFileSync(REPORT_PATH, md, 'utf8')
console.log(`  📄 Rapport : ${REPORT_PATH}`)
console.log('\n✓ Migration générée. Activez via SettingsTheme.')
